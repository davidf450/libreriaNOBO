public void filtro() {
        int columnaABuscar = 0;
        if (comboFiltro.getSelectedItem().equals("Codigo")) {
            columnaABuscar = 0;
        }
        if (comboFiltro.getSelectedItem().toString().equals("Nombre")) {
            columnaABuscar = 1;
        }
        if (comboFiltro.getSelectedItem().equals("Email")) {
            columnaABuscar = 2;
        }
        trsFiltro.setRowFilter(RowFilter.regexFilter(txtFiltro.getText(), columnaABuscar));
    }

    
