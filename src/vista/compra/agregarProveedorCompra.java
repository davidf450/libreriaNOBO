    private void formWindowOpened() throws Exception {                                  
        DefaultTableModel model = (DefaultTableModel) tablaListado.getModel();
        Object rowData[] = new Object[3];
        
        for(Proveedor p : ctrprov.buscarConConsultaNombrada("Proveedor.findAll")){
           rowData[0] = p.getCodigoProveedor();
           rowData[1] = p.getNombre_comercial();
           rowData[2] = p.getEmail();       
           model.addRow(rowData);
       }
       tablaListado.setModel(model);
   
    }
