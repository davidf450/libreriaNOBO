    private void botonEliminarSeleccionadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarSeleccionadosActionPerformed
        int target=tablaProductosCompra.getSelectedRow();
        Long codigoSeleccionado = (Long)tablaProductosCompra.getValueAt(target, 0);
        
        Iterator<Producto> iter=productos_a_cargar.iterator();
        while (iter.hasNext()) {
                Producto c = iter.next();
                if(codigoSeleccionado.equals(c.getCodigoProducto())){
                    iter.remove();
                    }
        }                
        
        System.out.println(this.productos_a_cargar.toString());
        ((DefaultTableModel)tablaProductosCompra.getModel()).removeRow(tablaProductosCompra.getSelectedRow());
    }//GEN-LAST:event_botonEliminarSeleccionadosActionPerformed

