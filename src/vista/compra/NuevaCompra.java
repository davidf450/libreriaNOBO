    private void botonGuardarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarVentaActionPerformed
        Compra nueva=new Compra();
        try{
            
            nueva.setFecha(boxFecha.getDate());
            if(P==null){
                JOptionPane.showMessageDialog(this,
                       "Por favor seleccione un proveedor!",
                       "Error!",
                        JOptionPane.ERROR_MESSAGE);
            }else{
            nueva.setProveedor(P);
            }
            nueva.setTotal(Double.parseDouble(campoTotal.getText()));

            //nueva.setProductos(productos_a_cargar);

            nueva.setNumero_factura(numeroFactura.getText());
            String tipoFactura = (String)campoTipoFactura.getSelectedItem();
            nueva.setTipo_factura(tipoFactura.charAt(0));
        }catch(Exception e){
            mostrarCartelError(this,"Ocurrio un error en la carga de algun dato");
        }
        
        for(int i=0;i<tablaProductosCompra.getRowCount();i++){
            Integer cant_a_sumar= (Integer)tablaProductosCompra.getValueAt(i, 5);
            Long codigo= (Long)tablaProductosCompra.getValueAt(i, 0);
            for(Producto P:this.productos_a_cargar){
                if(P.getCodigoProducto().equals(codigo)){
                         P.aumentarStock(cant_a_sumar);
                        //P.setStockActual(P.getStockActual()+cant_a_sumar);
                    try {
                        ctrpro.actualizar(P);
                    } catch (Exception e) {
                        e.printStackTrace();
                        mostrarCartelError(this,"Ocurrio un error al actualizar el stock");
                        
                    }
                    }
            }
            
        }
       
        //nueva.setProductos(productos_a_cargar);
        try{
            ctrcom.crear(nueva);
            for(int i=0;i<tablaProductosCompra.getRowCount();i++){
                LineaTransaccion nuevaLinea = new LineaTransaccion();
                nuevaLinea.setIdProducto((Long)tablaProductosCompra.getValueAt(i, 0));
                nuevaLinea.setNombreProducto((String)tablaProductosCompra.getValueAt(i, 1));
                nuevaLinea.setDescripcionProducto((String)tablaProductosCompra.getValueAt(i, 2));
                nuevaLinea.setUltimoCosto((Double)tablaProductosCompra.getValueAt(i, 3));
                nuevaLinea.setNuevoCosto((Double)tablaProductosCompra.getValueAt(i, 4));
                nuevaLinea.setCantidad((Integer)tablaProductosCompra.getValueAt(i, 5));
                nuevaLinea.setTotal((Double)tablaProductosCompra.getValueAt(i,  6));
                //nuevaLinea.setTransaccionPadre(nueva);
                ctrltr.crear(nuevaLinea);
                nueva.agregarLinea(nuevaLinea); 
            }
        }catch(Exception e){
            e.printStackTrace();
            mostrarCartelError(this,"Ocurrio un error al cargar las lineas");
            
        }
        try {
            ctrcom.actualizar(nueva);
            int respuesta=JOptionPane.showConfirmDialog(this,
                        "Se registro correctamente la compra" +
                        "¿Desea registrar otra?",
                        "Exito",
                        JOptionPane.YES_NO_OPTION);
                if(respuesta == JOptionPane.YES_OPTION){
                    limpiarCampos();
                        
                    }else{
                   super.dispose(); 
                }
        } catch (Exception e) {
            e.printStackTrace();
            mostrarCartelError(this,"Ocurrio un error al registrar la compra");
            
        }
        
       
    }//GEN-LAST:event_botonGuardarVentaActionPerformed

    private void botonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarProductoActionPerformed
        try {
            if(this.P==null){
                mostrarCartelError(this,"Primero debe seleccionar un proveedor!");
            }else{
            agregarProductoCompra apv = new agregarProductoCompra(this, rootPaneCheckingEnabled,P);
            apv.setVisible(rootPaneCheckingEnabled);
            Integer cantidad_producto=apv.getCantidad();
            Double costo_producto=apv.getCosto();
            Long codigo_buscado=apv.getCodigoSeleccionado();
            
            DefaultTableModel model = (DefaultTableModel) tablaProductosCompra.getModel();
            Object rowData[] = new Object[7];
            
            //Producto c = ctrpro.buscar(Producto.class, codigo_buscado);
            
            Producto c = apv.getP();
            
            this.productos_a_cargar.add(c);
            
            rowData[0] = c.getCodigoProducto();
            rowData[1] = c.getNombre();
            rowData[2] = c.getDescripcion();
            rowData[3] = c.getUltimoCosto();
            rowData[4] = costo_producto;
            if(c.getVieneEnCaja()){
                rowData[5] = cantidad_producto*c.getCantidadPorCaja();
            }else{
                rowData[5] = cantidad_producto;
            }
            rowData[6] = (costo_producto*cantidad_producto);
            model.addRow(rowData);
            tablaProductosCompra.setModel(model);
            
            Double total=0.0;
            for(int i=0;i<tablaProductosCompra.getRowCount();i++){
                total+= (Double)tablaProductosCompra.getValueAt(i, 6);
            }
            campoTotal.setText(String.valueOf(total));
            }
        } catch (Exception ex) {
            Logger.getLogger(NuevaCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_botonAgregarProductoActionPerformed

