    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try{                                         
        aModificar.setNombre(campoNombre.getText());
        aModificar.setMarca(campoMarca.getText());
        aModificar.setPrecioVenta(Double.parseDouble(campoPrecio.getText()));
        
        aModificar.setCodigoBarras(Long.parseLong(campoBarras.getText()));
        
        
        aModificar.setCodigoDeProveedor(campoCodPro.getText());
        Boolean habilitado = radioHabilitadoSi.isSelected();
        aModificar.setHabilitadoVenta(habilitado);
        
        Integer stock_minimo=Integer.parseInt(campoMinimo.getText());
        Integer stock_maximo=Integer.parseInt(campoMaximo.getText());
        if(stock_maximo<=stock_minimo){
            JOptionPane.showMessageDialog(this,
                    "El stock maximo no puede ser menor que el minimo",
                    "Error!",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(campoMarca.getText().isEmpty()
                || campoPrecio.getText().isEmpty() || campoNombre.getText().isEmpty()
                || campoCategoria.getText().isEmpty() || campoSubCategoria.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    "Se omitio uno o varios valores necesarios",
                    "Error!",
                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        }
        Integer stock_actual = Integer.parseInt(campoActual.getText());
        if(stock_actual<0){
            JOptionPane.showMessageDialog(this,
                    "El stock actual no puede ser menor que 0",
                    "Error!",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        aModificar.setStockActual(stock_actual);
        aModificar.setStockMaximo(Integer.parseInt(campoMaximo.getText()));
        aModificar.setStockMinimo(Integer.parseInt(campoMinimo.getText()));
        if(!campoCaja.getText().isEmpty()){
            aModificar.setCantidadPorCaja(Integer.parseInt(campoCaja.getText()));
            aModificar.setVieneEnCaja(true);
        }
        
      //ME QUEDE EN LAS CATEGORIAS
      
        if(nuevaCategoria!=null){
            aModificar.setCategoria(nuevaCategoria);
        }
        if(nuevaSubCategoria!=null){
            aModificar.setSubCategoria(nuevaCategoria);
        }
        List<Proveedor> aCargar = new ArrayList<>();
        if(!proveedoresSeleccionados.isEmpty()){
            for(Proveedor p : ctrprov.buscarConConsultaNombrada("Proveedor.findAll")){
                for(String s : proveedoresSeleccionados){
                
                    if(p.getNombre_comercial().equals(s)){
                        aCargar.add(p);
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar al menos un proveedor",
                    "Error!",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        for(Proveedor prov:aCargar){
            System.out.println(prov.getNombre_comercial());
        }
        aModificar.setProveedores(aCargar);
        
        ctrpro.actualizar(aModificar);
        
        super.dispose();
        
    }catch(Exception ex){
            
            Logger.getLogger(ModificarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }//GEN-LAST:event_jButton4ActionPerformed

