    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        List<Proveedor> proveedoresDelProducto = aModificar.getProveedores();
        for(Proveedor prov : proveedoresDelProducto){
            proveedoresSeleccionados.add(prov.getNombre_comercial());
            ((DefaultListModel)listaProveedoresSeleccionados.getModel()).addElement(prov.getNombre_comercial());
        }
        try {
            for(Proveedor prov : ctrprov.buscarConConsultaNombrada("Proveedor.findAll")){
                if(!proveedoresDelProducto.contains(prov)){
                    ((DefaultListModel)listaProveedoresDisponibles.getModel()).addElement(prov.getNombre_comercial());
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(ModificarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        campoNombre.setText(aModificar.getNombre());
        campoMarca.setText(aModificar.getMarca());
        campoPrecio.setText(String.valueOf(aModificar.getPrecioVenta()));
        campoActual.setText(String.valueOf(aModificar.getStockActual()));
        campoMinimo.setText(String.valueOf(aModificar.getStockMinimo()));
        campoMaximo.setText(String.valueOf(aModificar.getStockMaximo()));
        if(aModificar.getVieneEnCaja()){
            campoCaja.setText(String.valueOf(aModificar.getCantidadPorCaja()));
        }else{
            campoCaja.setText("");
        }
        if(aModificar.getHabilitadoVenta()){
            radioHabilitadoSi.setSelected(true);
        }else{
            radioHabilitadoNo.setSelected(true);
        }
        campoBarras.setText(String.valueOf(aModificar.getCodigoBarras()));
        if(aModificar.getCodigoDeProveedor()==null){
            campoCodPro.setText("");
        }else{
            campoCodPro.setText(String.valueOf(aModificar.getCodigoDeProveedor()));
        }
        campoCategoria.setText(aModificar.getCategoria().getNombreCategoria());
        campoSubCategoria.setText(aModificar.getSubCategoria().getNombreCategoria());
    }//GEN-LAST:event_formWindowOpened

