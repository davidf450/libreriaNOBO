    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if(agregarRecibo()){
            Recibo recibo2 = recibo;
            if(sv.hayOtroRecibo(recibo2.getFactura().getVenta()) + recibo2.getImporteTotalRecibo() 
                    >= recibo2.getFactura().getImporteTotal()){
                //recibo.getFactura().getVenta().setEstadoPago(true);
                recibo2.confirmarPago(recibo2.getFactura());
            }
            //recibo.getFactura().getVenta().getCliente().sumarImporte(recibo.getImporteCheque() + recibo.getImporteEfectivo());
            recibo2.sumarImporteCliente(recibo2.getImporteCheque() + recibo2.getImporteEfectivo());
            sv.addRecibo(recibo2);
            sv.guardarDatos();
            generarRecibo();
        }
            
    }//GEN-LAST:event_btnAceptarActionPerformed

