    private void completarCampos(){
        float importeRecibido = sv.hayOtroRecibo(recibo.getFactura().getVenta());
        if(importeRecibido == 0){
            cajaImporteVenta.setText(String.valueOf(recibo.getFactura().getImporteTotal()));
        }
        cajaFechaFactura.setText(recibo.getFactura().getFechaEmision());
        cajaFechaVenta.setText(recibo.getFactura().getVenta().getFechaVenta());
        cajaImporteVenta.setText(String.valueOf(recibo.getFactura().getImporteTotal() - importeRecibido));
        cajaImporteEfectivo.setText("0.00");
    }
    
