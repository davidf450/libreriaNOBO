    private boolean agregarRecibo(){
        if(cajaImporteEfectivo.getText().trim().isEmpty() && cajaNroCheque.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar algún valor!", "Hubo un problema..", JOptionPane.WARNING_MESSAGE);
            return false;
        }else if(!comboCheque.isSelected()){
            valorEfectivo = Float.parseFloat(cajaImporteEfectivo.getText().trim());
            if(valorEfectivo > 0){
                recibo.setImporteEfectivo(valorEfectivo);
                recibo.setNroCheque("-");
                recibo.setImporteCheque(0.00f);
                return true;
            }else{
                JOptionPane.showMessageDialog(this, "Ingresó un valor nulo!", "Hubo un problema..", JOptionPane.WARNING_MESSAGE);
                return false;
            }  
        }else{
            valorEfectivo = Float.parseFloat(cajaImporteEfectivo.getText().trim());
            valorCheque = Float.parseFloat(cajaImporteCheque.getText().trim());
            if(!cajaNroCheque.getText().isEmpty() && valorCheque > 0){
                recibo.setImporteEfectivo(valorEfectivo);
                recibo.setImporteCheque(valorCheque);
                recibo.setNroCheque(cajaNroCheque.getText().trim());
                if(recibo.getNroCheque().isEmpty())
                    recibo.setNroCheque("-");
                return true;
            }else{
                JOptionPane.showMessageDialog(this, "Verifique los datos del cheque y los importes!", "Hubo un problema..", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }
    }
    
    private void generarRecibo(){
        try {
            NumeroALetra numLetra = new NumeroALetra();
            DecimalFormat df = new DecimalFormat("0.00");
            String importe = String.valueOf(df.format(recibo.getImporteTotalRecibo()));
            JasperReport jr = (JasperReport)JRLoader.loadObject
            (this.getClass().getResource("/reporte/recibo.jasper"));
            //JasperReport jr = JasperCompileManager.compileReport(getClass()
            //        .getResource("/reporte/facturaA.jrxml").getPath());
            Map mapa = new HashMap();
            JasperPrint jp;
            mapa.put("domicilioEmpresa", sv.getDomicilio());
            mapa.put("nombreEmpresa", sv.getNombre());
            mapa.put("condicionIVAEmpresa", sv.getCondicionIva());
            mapa.put("puntoDeVentaEmpresa", String.format("%04d", sv.getPuntoVenta()));
            mapa.put("nroRecibo", String.format("%08d", recibo.getNroRecibo()));
            mapa.put("cuitEmpresa", sv.getCuit());
            mapa.put("fechaInicioActividades", sv.getFechaInicio());
            mapa.put("nombreEmpresaCliente", recibo.getFactura().getVenta().getCliente().getNombre());
            mapa.put("importeEnLetras", numLetra.Convertir(importe, false));
            mapa.put("fechaFactura", recibo.getFactura().getFechaEmision());
            mapa.put("nroFactura", String.format("0%08d", recibo.getFactura().getNroFactura()));
            mapa.put("importeFactura", String.valueOf(recibo.getFactura().getImporteTotal()));
            mapa.put("importeEfectivo", String.valueOf(recibo.getImporteEfectivo()));
            mapa.put("importeCheque", String.valueOf(recibo.getImporteCheque()));
            mapa.put("nroCheque", recibo.getNroCheque());
            mapa.put("importeTotal", String.valueOf(recibo.getImporteTotalRecibo()));
            
            jp = JasperFillManager.fillReport(jr, mapa, new JREmptyDataSource());
            visor = new JasperViewer(jp,false);
            visor.setVisible(true);
            this.dispose();
            visor.toFront();
        }catch (JRException ex) {
            Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
        }
    }
    
