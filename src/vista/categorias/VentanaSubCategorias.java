    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Long codigoSeleccionado = (Long) tablaSubCategorias.getValueAt(tablaSubCategorias.getSelectedRow(), 0);
        
        Categoria aBorrar;
        try {
            DefaultTableModel tableModel = (DefaultTableModel) tablaSubCategorias.getModel();
            aBorrar = ctrcat.buscar(Categoria.class, codigoSeleccionado);
            ctrcat.borrar(aBorrar);
            tableModel.removeRow(tablaSubCategorias.getSelectedRow());
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                        "Existen productos pertenecientes a esta Subcategoria",
                        "Error al borrar",
                        JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void botonCambiarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCambiarNombreActionPerformed
        String nuevoNombre = JOptionPane.showInputDialog(
                "Ingrese el nuevo nombre para '"+tablaSubCategorias.getValueAt(tablaSubCategorias.getSelectedRow(), 1)+"'");
        
        try {
            Categoria aModificar = ctrcat.buscar(Categoria.class,(Long)tablaSubCategorias.getValueAt(tablaSubCategorias.getSelectedRow(), 0));
            aModificar.setNombreCategoria(nuevoNombre);
            ctrcat.actualizar(aModificar);
            tablaSubCategorias.setValueAt(nuevoNombre,tablaSubCategorias.getSelectedRow(), 1);
        } catch (Exception ex) {
            Logger.getLogger(VentanaSubCategorias.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonCambiarNombreActionPerformed
    private void formWindowOpened() {
        DefaultTableModel model = (DefaultTableModel) tablaSubCategorias.getModel();
        Object rowData[] = new Object[2];
        
        for(Categoria cat : this.categoria.getSubCategorias()){
           rowData[0] = cat.getCodigoCategoria();
           rowData[1] = cat.getNombreCategoria();
                  
           model.addRow(rowData);
        
       }
       tablaSubCategorias.setModel(model);
   
    }   
