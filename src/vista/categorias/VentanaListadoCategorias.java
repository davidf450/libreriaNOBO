    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        Long codigoSeleccionado = (Long) tablaCategorias.getValueAt(tablaCategorias.getSelectedRow(), 0);
        
        Categoria aBorrar;
        try {
            DefaultTableModel tableModel = (DefaultTableModel) tablaCategorias.getModel();
            aBorrar = ctrcat.buscar(Categoria.class, codigoSeleccionado);
            ctrcat.borrar(aBorrar);
            tableModel.removeRow(tablaCategorias.getSelectedRow());
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                        "Existen productos pertenecientes a esta categoria",
                        "Error al borrar",
                        JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_botonEliminarActionPerformed

    private void botonCambiarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCambiarNombreActionPerformed
        String nuevoNombre = JOptionPane.showInputDialog(
                "Ingrese el nuevo nombre para '"+tablaCategorias.getValueAt(tablaCategorias.getSelectedRow(), 1)+"'");
        
        try {
            Categoria aModificar = ctrcat.buscar(Categoria.class,(Long)tablaCategorias.getValueAt(tablaCategorias.getSelectedRow(), 0));
            aModificar.setNombreCategoria(nuevoNombre);
            ctrcat.actualizar(aModificar);
            tablaCategorias.setValueAt(nuevoNombre,tablaCategorias.getSelectedRow(), 1);
        } catch (Exception ex) {
            Logger.getLogger(VentanaListadoCategorias.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonCambiarNombreActionPerformed
    private void formWindowOpened() throws Exception {                                  
        DefaultTableModel model = (DefaultTableModel) tablaCategorias.getModel();
        Object rowData[] = new Object[2];
        for(Categoria cat : ctrcat.buscarConConsultaNombrada("Categoria.findPrincipales")){
           rowData[0] = cat.getCodigoCategoria();
           rowData[1] = cat.getNombreCategoria();
           
           
           model.addRow(rowData);
        
       }
       tablaCategorias.setModel(model);
   
    }   
