    private void agregarProducto(Long codigoSeleccionado){
        try{
            cantidad= Integer.parseInt(JOptionPane.showInputDialog("Cantidad?"));

            Producto aux = ctrpro.buscar(Producto.class, codigoSeleccionado);
            /*for(Producto P : ctrpro.buscarConConsultaNombrada("Producto.findProductos")){
                if(P.getCodigoProducto().equals(codigoSeleccionado)){*/

                    if((aux.getStockActual()- cantidad) < aux.getStockMinimo()){
                        if((aux.getStockActual()- cantidad) < 0){
                            JOptionPane.showMessageDialog(this,
                                "No hay stock suficiente del articulo solicitado, quedan "+aux.getStockActual()+" disponibles",
                                "Advertencia!",
                                JOptionPane.INFORMATION_MESSAGE);
                            return;
                        }else{
                        if(!aux.getFaltante()){
                        JOptionPane.showMessageDialog(this,
                                "El producto seleccionado quedara por debajo del Stock minimo ",
                                "Advertencia!",
                                JOptionPane.INFORMATION_MESSAGE);
                        }
                        if(aux.getStockActual()-cantidad <= (aux.getStockMinimo()*20)/100){
                            JOptionPane.showMessageDialog(this,
                                "El producto seleccionado quedara por debajo de un nivel critico, se lo inhabilitara para la venta ",
                                "Advertencia!",
                                JOptionPane.INFORMATION_MESSAGE);
                        }
                        aux.setFaltante(true);
                        P = aux;
                        }
                    }else{
                        P=aux;
                    }
                    
                    if((cantidad.equals(aux.getStockActual()))){
                        JOptionPane.showMessageDialog(this,
                                "Con esta venta se agotan las existencias del producto",
                                "Advertencia!",
                                JOptionPane.INFORMATION_MESSAGE);
                        aux.setFaltante(true);
                        aux.setHabilitadoVenta(false);
                        P = aux;
                    }
             
            super.dispose();
        } catch (Exception ex) {
            Logger.getLogger(agregarProductoVenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
