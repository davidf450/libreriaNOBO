    private void formWindowOpened() {
        DefaultTableModel model = (DefaultTableModel) tablaProveedores.getModel();
        Object rowData[] = new Object[5];
        System.out.println(producto.getProveedores().size());
        //Set<Proveedor> proveedores = new HashSet<Proveedor>(producto.getProveedores());
        for(Proveedor prov : producto.getProveedores()){
           rowData[0] = prov.getCodigoProveedor();
           rowData[1] = prov.getNombre_comercial();
           rowData[2] = prov.getTelefono1();
           rowData[3] = prov.getNombre_contacto();
           rowData[4] = prov.getEmail();        
           model.addRow(rowData);
        
       }
       tablaProveedores.setModel(model);
   
    }   
